        exit 1
    fi
    
    # –í—ã–ø–æ–ª–Ω—è–µ–º —à–∞–≥–∏ –º–∏–≥—Ä–∞—Ü–∏–∏
    check_dependencies
    install_dependencies
    setup_railway
    setup_environment
    deploy_to_railway
    
    # –ñ–¥–µ–º, –ø–æ–∫–∞ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –∑–∞–ø—É—Å—Ç–∏—Ç—Å—è
    log "–û–∂–∏–¥–∞–Ω–∏–µ –∑–∞–ø—É—Å–∫–∞ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è..."
    sleep 30
    
