        echo "ü§ñ –û–±–Ω–æ–≤–∏—Ç–µ webhook –≤ BotFather: $APP_URL/api/telegram/webhook"
        echo "üìù –û–±–Ω–æ–≤–∏—Ç–µ Menu Button –≤ BotFather: $APP_URL"
    fi
    
